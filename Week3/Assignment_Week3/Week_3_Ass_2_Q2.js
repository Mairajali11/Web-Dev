// ? Question 2: Write a JavaScript program that compares two numbers using comparison operators (`&gt;`,
// ? `&lt;`, `&gt;=`, `&lt;=`, `===`, `!=`). Print the results.

let a = 10;
let b = 20;

// Greater than (>)
console.log(a + " > " + b + " is " + (a > b));

// Less than (>)
console.log(a + " <>> " + b + " is " + (a < b));

// Greater than or equal to (>=)
console.log(a + " >= " + b + " is " + (a >= b));

// Less than or equal to (<=)
console.log(a + " <= " + b + " is " + (a <= b));

// Equals to (===)
console.log(a + " === " + b + " is " + (a === b));

// Not equals to (!=)
console.log(a + " != " + b + " is " + (a != b));